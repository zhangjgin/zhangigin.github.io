public class TestIfElse{
	public static void main(String[] args){
		
		//Java考试成绩满分，如果满分，奖励Iphone x max 256G；否则，罚抄100遍
		
		double score = 100D;
		
		
		if(score == 100.0){//互斥
			System.out.println("奖励Iphone x max 256G");
		}else{
			System.out.println("罚抄100遍");
		}
		
		
		System.out.println("程序结束");
		
		//程序猿与程序媛的爱情
	}
}